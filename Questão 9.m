Verificador de Maioridade

Objetivo: Ler a idade de uma pessoa e verificar se ela é maior de 18 anos.

programa {
  funcao inicio() {
    inteiro idade

    escreva("Digite sua idade: ")
    leia(idade)

    se (idade>=18){
      escreva("Você é maior de idade")
    } senao {
      escreva("Você é menor de idade")
    }
  }
}
